void main(){

  //only applicable for int and String

  String grade = 'A';

  switch (grade)
      {
        case 'A' :
          print("Excellent grade A");
          break;
        case 'B' :
          print("Good grade B");
          break;
        case 'C' :
          print("Average grade C");
          break;
        case 'D' :
          print("Poor grade D");
          break;
    default:
      print("You have failed");

  }
}
